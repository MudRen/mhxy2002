#include <ansi.h>

inherit SSERVER;

int perform(object me, object target)
{
        object weapon;
        int i;
        if( !target ) target = offensive_target(me);

        if( !target
        ||      !target->is_character()
        ||      target->is_corpse()
        ||      target==me)
                return notify_fail("你要对谁施展这一招「情愁一刀」？\n");

        if(!me->is_fighting())
                return notify_fail("「情愁一刀」只能在战斗中使用！\n");

        if(me->query("family/family_name") != "月宫" )
                return notify_fail("你是哪儿偷学来的武功，也想使用！\n");

        if((int)me->query("max_force") < 1000 )
                return notify_fail("你的内力不够！\n");

        if((int)me->query("force") < 500 )
                return notify_fail("你的内力不足！\n");

        if((int)me->query("sen") < 500 )
                return notify_fail("你的精神不足，没法子施用外功！\n");
             
               if (me->query_skill_mapped("force")!="moonforce")
                return notify_fail("情愁一刀必须配合圆月心法才能使用。\n");


   i=(int)me->query_skill("xuanhu-blade",1)+(int)me->query_kar();

        if( i < 120)
                return notify_fail("你的玄狐长恨刀级别还不够，使用这一招会有困难！\n");

   message_vision(HIC"\n$N眼中含泪,情愁化为愤怒,一切的愤怒化为刀力,使出了情愁一刀!!!\n"NOR,me);

   me->delete("env/brief_message");
   target->delete("env/brief_message");

        me->set_temp("XFJ_perform", 9);
        COMBAT_D->do_attack(me, target, me->query_temp("weapon"));

        me->delete_temp("XFJ_perform");

        me->receive_damage("sen", 100+random(100));
        me->add("force", -200);

        if( !target->is_fighting(me) ) {
                if( living(target) ) {
                        if( userp(target) ) target->fight_ob(me);
                        else target->kill_ob(me);
                }
        }

        me->start_busy(2);
        return 1;
}